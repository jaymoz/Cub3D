#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	c = (char)c;
	i = ft_strlen(s);
	while (i > 0)
	{
		if (s[i] == c)
			return ((char *)(s + i));
		i--;
	}
	if (s[i] == c)
		return ((char *)s);
	return (0);
}
