#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*tmp;
	unsigned int	i;

	if (s == 0 || f == 0)
		return (0);
	tmp = (char *)malloc(ft_strlen(s) + 1);
	if (tmp == 0)
		return (0);
	i = 0;
	while (s[i])
	{
		tmp[i] = f(i, s[i]);
		i++;
	}
	tmp[i] = 0;
	return (tmp);
}
