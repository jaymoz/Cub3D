#include "libft.h"

char	*ft_strdup(char *src)
{
	size_t	len;
	char	*tmp;

	len = ft_strlen(src);
	tmp = (char *)malloc(len + 1);
	if (tmp == 0)
		return (0);
	ft_strlcpy(tmp, src, len + 1);
	return (tmp);
}
