#include "libft.h"

static void	ft_putchar(char c, int fd)
{
	write(fd, &c, 1);
}

static void	ft_printnbr(int n, int fd)
{
	if (n < 10)
		ft_putchar(n + 48, fd);
	else
	{
		ft_printnbr(n / 10, fd);
		ft_putchar(n % 10 + 48, fd);
	}
}

void	ft_putnbr_fd(int n, int fd)
{
	if (fd < 0)
		return ;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	else if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	ft_printnbr(n, fd);
}
